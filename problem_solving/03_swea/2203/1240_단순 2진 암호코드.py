# 8자리
# 고유번호 7자리 + 검증코드
                # “(홀수 자리의 합 x 3) + 짝수 자리의 합 + 검증 코드” 가 10의 배수
# if not -> 비정상


pw = {
    "0001101" : 0,
    "0011001" : 1,
    "0010011" : 2,
    "0111101" : 3,
    "0100011" : 4,
    "0110001" : 5,
    "0101111" : 6,
    "0111011" : 7,
    "0110111" : 8,
    "0001011" : 9
}
t = int(input())
for tc in range(1, t+1):
    n, m = map(int, input().split())
    #n개의 줄
    input_arr = [input() for _ in range(n)]
    #pprint.pprint(input_arr)

    s = 0
    for i in range(n):
        arr = [] #한 배열당 암호코드를 담을 곳
        j = m-1
        while j>-1:
            if input_arr[i][j] == '1':
                arr.append(input_arr[i][j-6:j+1]) # 8개가 들어가므로,순서고려 x 마지막부터들어감
                j-=7
            else:
                j-=1

        odd = 0
        even = 0
        lastcode = 0  #마지막 코드가 idx 0에 있음
        # 마지막 코드가 짝수이므로, 마지막 코드까지 더했을때 10의 배수이면 출력함
        if arr:
            for i in range(len(arr)-1, -1, -1):  #7, -1, -1
                if i==0:
                    lastcode = pw[arr[i]]
                if i%2==0: #
                    even += pw[arr[i]]
                else:
                    odd += pw[arr[i]]

            res = odd*3 + even
            if res%10==0:
                s = odd+even


    print(f'#{tc} {s}')





